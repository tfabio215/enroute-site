        <h3>Health Benefits</h3>
        <h4>Cardiovascular Health</h4>
        <p>Canola oil has been shown in multiple studies to reduce total and LDL cholesterol levels while maintaining HDL cholesterol. Studies also indicate it may inhibit platelet aggregation, helping to prevent the formation of arterial clots. Animal studies suggest possible protection against cardiac arrhythmias.</p>
        <h4>Omega-3 Fatty Acids</h4>
        <p>The omega-3 fatty acids in canola oil have demonstrated benefits across cardiovascular, immune/inflammatory, and neurological systems. They are also being actively investigated as potential anti-cancer agents. Canola oil's favourable omega-6 to omega-3 ratio (approximately 2:1) is particularly notable.</p>
        <h4>Blood Glucose &amp; Diabetes</h4>
        <p>High monounsaturated fat diets may benefit blood glucose control in people with type 2 diabetes. Research has shown canola oil as part of a Mediterranean-style diet can improve insulin sensitivity markers.</p>

        <h3>Safety and Side Effects</h3>
        <h4>Erucic Acid</h4>
        <p>Traditional rapeseed contains 20–55% erucic acid — a compound linked to cardiac lipidosis in animal studies at high doses. Canola oil contains less than 2% erucic acid and is considered safe for human consumption by Health Canada and the FDA.</p>
        <h4>Side Effects</h4>
        <p>No documented side effects when consumed normally with food as part of a balanced diet. Large amounts consumed on an empty stomach may cause temporary digestive discomfort in sensitive individuals.</p>

        <h3>Canola and Genetic Modification</h3>
        <h4>Origins of Canola</h4>
        <p>Canola was developed through traditional selective plant breeding — not genetic modification. It was created by Canadian researchers in the 1970s to produce a rapeseed variety with low erucic acid and low glucosinolates.</p>
        <h4>Genetically Modified Varieties</h4>
        <p>Several approved GM varieties of canola now exist in Canada and the USA, including herbicide-resistant (Roundup Ready) and pollination-controlled varieties. These have been thoroughly tested and are considered as safe as conventional canola by regulatory agencies.</p>
        <h4>Non-GMO Options</h4>
        <p>For customers requiring Non-GMO verified products, Enroute Imports offers Non-GMO Canola Oil that meets Non-GMO Project standards and is available with full traceability documentation. Our Organic Canola is also inherently Non-GMO certified.</p>
        <h4>Labelling</h4>
        <p>In Canada, mandatory GM labelling applies when food composition is materially modified or when safety concerns exist. Plants modified only for agronomic traits without affecting food composition, nutritional content, or safety do not require GM labelling under current Canadian regulations.</p>
