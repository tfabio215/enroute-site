        <h2>Canola Oil</h2>
        <h3>Introduction</h3>
        <h4>Background</h4>
        <p><img src="images/mini_poster1.jpg" align="right" alt="Canola oil nutrition poster" onerror="this.style.display='none'">Canola is a "double-low" variety of the rapeseed plant (<em>Brassica napus</em> or <em>Brassica campestris</em>) developed by traditional plant breeding methods in the 1970s. Compared to rapeseed, canola contains reduced levels of erucic acid and glucosinolates — making it safe and nutritious for human consumption.</p>
        <h4>Uses</h4>
        <p>Canola oil is extracted from the seed of the canola plant. Widely used in salad dressings, margarines, and shortenings. Suitable for cooking and baking at temperatures below 360°F (182°C). The seed meal remaining after extraction is used extensively in animal feed.</p>
        <h4>Cost &amp; Availability</h4>
        <p>Very inexpensive compared to most vegetable oils — much cheaper than olive oil and peanut oil, similar in price to soybean and corn oil. Widely available year-round from Canadian and international origins.</p>

        <h3>Nutritional Information</h3>
        <h4>Monounsaturated Fat</h4>
        <p>An excellent source of monounsaturated fat, containing almost as much as olive oil. Monounsaturated fats are considered beneficial when they replace saturated fats in the diet, recommended at 10–15% of total daily calories.</p>
        <h4>Essential Fatty Acids</h4>
        <p>Provides both linoleic acid (LA, omega-6) and alpha-linolenic acid (ALA, omega-3) — essential fatty acids the body cannot produce independently. These must be obtained from dietary sources.</p>
        <h4>Low in Saturated Fat</h4>
        <p>Very low in saturated fat at approximately 7% of total fat content. Health authorities recommend that no more than 10% of total calories come from saturated fat.</p>
        <h4>Vitamin E</h4>
        <p>One tablespoon of canola oil provides approximately 2.9 mg of vitamin E (alpha-tocopherol) — roughly one-fifth of the recommended daily intake for adults.</p>
        <h4>Phytosterols</h4>
        <p>A good source of phytosterols, plant compounds that compete with cholesterol for absorption in the digestive system. Research indicates phytosterols can help reduce LDL cholesterol levels when consumed as part of a balanced diet.</p>

